
public class Datas {
	public static void main(String []args) {
		int num1 = 2147483647;
		short num2 = 32767;
		float num3 = 0.5f;
		double output = 19.891;
		char alpha = 'A';
		boolean result = true;
		
		System.out.println("Integer is " + num1);
		System.out.println("Short is " + num2);
		System.out.println("Float is " + num3);
		System.out.println("Double is " + output);
		System.out.println("Char is " + alpha);
		System.out.println("Boolean is " + result);		
	}
}
 